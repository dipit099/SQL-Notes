--for each department, print the number of employees working in that dept

SELECT D.DEPARTMENT_ID, COUNT(E.EMPLOYEE_ID) AS N_EMPLOYEES
FROM EMPLOYEES E RIGHT JOIN DEPARTMENTS D ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
GROUP BY D.DEPARTMENT_ID;
































