SELECT M.EMPLOYEE_ID, M.FIRST_NAME, M.LAST_NAME, COUNT(E.MANAGER_ID), COUNT(*)
FROM EMPLOYEES M LEFT JOIN EMPLOYEES E ON M.EMPLOYEE_ID= E.MANAGER_ID -- E HOLO MANAGER KRE
GROUP BY M.EMPLOYEE_ID, M.FIRST_NAME, M.LAST_NAME
ORDER BY M.EMPLOYEE_ID;

